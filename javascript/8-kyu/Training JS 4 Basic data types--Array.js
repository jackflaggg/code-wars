function getLength(arr){
	//return length of arr
	return arr.length;
}
function getFirst(arr){
	//return the first element of arr
	return arr.at(0)
}
function getLast(arr){
	//return the last element of arr
	return arr.at(-1)
}
function pushElement(arr){
	let el=1;
	//push el to arr
	arr.push(el)
	return arr
}
function popElement(arr){
	arr.pop()
	return arr
}